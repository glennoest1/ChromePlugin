let bbbCurrentLanguage = "en";

function bbbNormalizeLanguage(language) {
  const value = String(language || "").toLowerCase();
  if (value.startsWith("vi")) return "vi";
  if (value.startsWith("zh") || value.startsWith("cn")) return "zh";
  return "en";
}

async function bbbInitI18n() {
  const stored = await chrome.storage.local.get(BBB_LANGUAGE_STORAGE_KEY).catch(() => ({}));
  const browserLanguage = chrome.i18n?.getUILanguage?.() || navigator.language;
  bbbCurrentLanguage = bbbNormalizeLanguage(stored[BBB_LANGUAGE_STORAGE_KEY] || browserLanguage);
  document.documentElement.lang = bbbCurrentLanguage === "zh" ? "zh-CN" : bbbCurrentLanguage;
  document.dispatchEvent(new CustomEvent("bbb-language-ready", { detail: { language: bbbCurrentLanguage } }));
  return bbbCurrentLanguage;
}

function bbbT(key, params = {}) {
  const dictionary = BBB_TRANSLATIONS[bbbCurrentLanguage] || BBB_TRANSLATIONS.en;
  let value = dictionary[key] || BBB_TRANSLATIONS.en[key] || key;
  for (const [name, replacement] of Object.entries(params)) {
    value = value.replaceAll(`{${name}}`, String(replacement));
  }
  return value;
}

function bbbRenderLanguageSelect(id = "languageSelect") {
  const current = bbbCurrentLanguage;
  return `
    <label class="language-control" for="${id}">
      <span>${escapeHtml(bbbT("languageLabel"))}</span>
      <select id="${id}">
        <option value="en"${current === "en" ? " selected" : ""}>${escapeHtml(bbbT("english"))}</option>
        <option value="vi"${current === "vi" ? " selected" : ""}>${escapeHtml(bbbT("vietnamese"))}</option>
        <option value="zh"${current === "zh" ? " selected" : ""}>${escapeHtml(bbbT("chinese"))}</option>
      </select>
    </label>`;
}

function bbbWireLanguageSelect(id = "languageSelect") {
  const select = document.getElementById(id);
  if (!select) return;
  select.addEventListener("change", async () => {
    await chrome.storage.local.set({ [BBB_LANGUAGE_STORAGE_KEY]: bbbNormalizeLanguage(select.value) });
    window.location.reload();
  });
}
